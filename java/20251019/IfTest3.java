public class IfTest3 {

    public static void main(String[] args) {
        int myAge = 29;
        if (myAge == 29) {
            System.out.println("私の年齢は29歳！");
        } else if (myAge >= 40) {
            System.out.println("私の年齢は40歳以上です！");
        } else {
            System.out.println("私の年齢は29歳でも40歳以上でもないです！");
        }
        System.out.println("私の出身は神奈川です。");
    }
}
